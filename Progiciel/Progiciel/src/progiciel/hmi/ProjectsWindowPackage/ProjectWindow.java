    private void cnlBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cnlBtnActionPerformed
        boolean res = this.project.Cancel();
        
        if(res == true){
            DeleteConfirm delete = new DeleteConfirm(this.user);
            delete.setVisible(true);
            dispose();
        }
        
    }//GEN-LAST:event_cnlBtnActionPerformed

    private void valBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_valBtnActionPerformed
        boolean res = this.project.Validated();
        
        if(res == true){
            ValidatedConfirm validated = new ValidatedConfirm(this.user);
            validated.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_valBtnActionPerformed

    private void supportBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_supportBtnActionPerformed
        Desktop desktop = Desktop.getDesktop();
        String message = "mailto:Valentin_Marguerie@etu.u-bourgogne.fr?subject=Support%20Email";
        URI uri = URI.create(message);
        try {
            desktop.mail(uri);
        } catch (IOException ex) {
            Logger.getLogger(ProjectsWindow.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_supportBtnActionPerformed

